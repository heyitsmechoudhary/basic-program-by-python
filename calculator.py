# Python Arithmetic Operators( +,-,*,/,%,**,//)
a=float(input("enter 1st number : "))
op=input("enter your opration : ")
b=float(input("enter 2nd number : "))
if op=="+":
    print(a+b)
elif op=="-":
    print(a-b)
elif op=="*":
    print(a*b)
elif op=="/":
    print(a/b)
elif op==" % ":
    print(a%b)  
elif op==" ** ":
    print(a**b)  
elif op==" // ":
    print(a//b)  
else:
    print("please enter a valid opration")
